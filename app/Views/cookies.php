<h2 class="mt-4">¿Cómo administrar cookies en el navegador?</h2>
<p>El usuario tiene la opción de permitir, bloquear o eliminar las cookies instaladas en su equipo mediante la configuración de las opciones del navegador instalado en su terminal:</p>

<h3 class="mt-4">Para ajustar los permisos relacionados con las cookies en el navegador Google Chrome:</h3>
<ol>
  <li>Hacer clic en el menú situado en la barra de herramientas.</li>
  <li>Seleccionar Configuración.</li>
  <li>Hacer clic en Mostrar opciones avanzadas.</li>
  <li>En la sección "Privacidad" hacer clic en el botón Configuración de contenido.</li>
  <li>En la sección de cookies se pueden configurar las opciones.</li>
  <li><a href="https://support.google.com/chrome/answer/95647?hl=es" target="_blank">Más información sobre Google Chrome</a></li>
</ol>

<h3 class="mt-4">Para ajustar los permisos relacionados con las cookies en el navegador Mozilla Firefox:</h3>
<ol>
  <li>En la parte superior de la ventana de Firefox hacer clic en el menú Herramientas.</li>
  <li>Seleccionar Opciones.</li>
  <li>Seleccionar el panel Privacidad.</li>
  <li>En la opción Firefox podrá elegir Usar una configuración personalizada para el historial para configurar las opciones.</li>
  <li><a href="https://support.mozilla.org/es/kb/habilitar-y-deshabilitar-cookies-sitios-web-rastrear-preferencias" target="_blank">Más información sobre Mozilla Firefox</a></li>
</ol>

<h3 class="mt-4">Para ajustar los permisos relacionados con las cookies en el navegador Microsoft Edge:</h3>
<ol>
  <li>En la parte superior de la ventana de Edge hacer clic en el menú de tres puntos.</li>
  <li>Seleccionar Configuración.</li>
  <li>Seleccionar la pestaña de Privacidad, búsqueda y servicios.</li>
  <li>En la sección "Cookies y otros datos del sitio" configurar las opciones deseadas.</li>
  <li><a href="https://support.microsoft.com/es-es/microsoft-edge/eliminar-las-cookies-en-microsoft-edge-63947406-40ac-c3b8-57b9-2a946a29ae09" target="_blank">Más información sobre Microsoft Edge</a></li>
</ol>

